int	ft_str_is_alpha(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (!(str[i] >= 'a' && str[i] <= 'z')
			&& !(str[i] >= 'A' && str[i] <= 'Z'))
			return (0);
		i++;
	}
	return (1);
}

#include <stdio.h>

int main()
{
	char	x[] = "bhjbADFAKSJF";

	printf("%d", ft_str_is_alpha(x));
	return (0);
}
